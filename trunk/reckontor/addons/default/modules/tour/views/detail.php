                <div class="cms-content mhm mtm">Experience the splendor of Denali National Park from the air and the ground on this day trip from Anchorage. Your 13-hour tour begins bright and early with a scenic 75-minute flight to the park. As the plane approaches North America’s highest mountain, Mt McKinley (the English name for Denali), watch for climbers on their way to the summit and enjoy bird’s-eye views of multiple glaciers. <br><br>Once you've landed inside the park, board your coach to begin your comprehensive park tour. Denali National Park contains more than 6 million acres (2.4 million hectares) of Alaskan wilderness, including Mt McKinley, and is home to an abundance of Alaskan birds and mammals, some of the largest being caribou, moose, Dall sheep and grizzly bears. During the bus portion of the tour (six to eight hours, depending on traffic or animal sightings), your driver-guide will help you spot wildlife and answer questions about the park. <br><br>Then, once you have a good feel for the area, you will have free time to explore on your own. Use the park’s shuttle (included) to start a hike or find the perfect spot for a picnic. You’ll receive a box lunch with a sandwich and snacks, and there are gift shops and snack shops throughout the park if you’d like to purchase more food or souvenirs.<br><br>After your day of sightseeing in Denali National Park, take a second flight back to Anchorage. This flight will take a different route than the first one, giving you a new perspective of the area.</div>
				
                
